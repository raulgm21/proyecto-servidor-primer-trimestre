window.onload = function (){

    // --- APARTADO DE LO QUIERO --- //
    document.getElementById('quieroBoton').addEventListener('click',mostrar,false);       
    document.getElementById('cerrarBoton').addEventListener('click',cerrarBoton,false);     
     
}

function mostrar(){
    
    window.mostrarFormu.showModal();
}

function cerrarBoton(){
    window.mostrarFormu.close();
}